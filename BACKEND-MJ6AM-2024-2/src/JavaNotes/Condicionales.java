package JavaNotes;

public class Condicionales {
    public static void main(String[] args) {
        int numero = 1;
        switch (numero){
            case 1:
                System.out.println("Elegiste el numero 1");
                break;
            case 2:
                System.out.println("Elegiste el numero 2");
                break;
            case 3:
                System.out.println("Elegiste el numero 3");
                break;
            default:
                System.out.println("Opcion no valida");
        }
    }
}
